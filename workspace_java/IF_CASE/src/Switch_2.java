public class Switch_2 {
  public static void main(String[] args) {
    String a = "땡중";

    switch (a) {
      case "남" :
        System.out.println("남성입니다.");
        break;
      case "여" :
        System.out.println("여성입니다.");
        break;
      default :
        System.out.println("혼성입니다.");
    }
  }
}
